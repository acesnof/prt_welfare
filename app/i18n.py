def get_language():
    lang = get_setting("lingua", LANG_PT) or LANG_PT
    return lang if lang in TRANSLATIONS else LANG_PT


def t(key, **kwargs):
    text = TRANSLATIONS.get(get_language(), TRANSLATIONS[LANG_PT]).get(key, TRANSLATIONS[LANG_PT].get(key, key))
    return text.format(**kwargs) if kwargs else text

